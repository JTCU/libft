#include "libft.h"

static int	ft_abs(int nbr)
{
	if (nbr > 0)
		return (nbr);
	else
		return (-nbr);
}

static void	ft_strrev(char *str)
{
	size_t	len;
	size_t	i;
	char	temp;

	len = ft_strlen(str);
	i = 0;
	while (i < len / 2)
	{
		temp = str[i];
		str[i] = str[len - i - 1];
		str[len - i - 1] = temp;
		i++;
	}
}

char	*ft_itoa(int n)
{
	char	*str;
	int		neg;
	size_t	len;

	neg = (n < 0);
	str = ft_calloc(11 + neg, sizeof(*str));
	if (!str)
		return (NULL);
	if (n == 0)
		str[0] = '0';
	len = 0;
	while (n != 0)
	{
		str[len++] = '0' + ft_abs(n % 10);
		n = (n / 10);
	}
	if (neg)
		str[len] = '-';
	ft_strrev(str);
	return (str);
}
